    private void llenarTabla() {
        List<Permiso> arregloPermisos = objMiDao.consultar("");
        for (Permiso objPermiso : arregloPermisos) {
            Object[] objFile = new DaoPermiso[13];
            objFile[0] = objPermiso.getCodPermiso();
            objFile[1] = objPermiso.getNombrePermiso();
            objFile[2] = objPermiso.getFuncionalidadPermiso();
           
            miModeloTabla.addRow(objFile);

        }
    }
